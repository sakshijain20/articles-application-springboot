export class Comment {

    id?: any;
    commentString?: string;
    created?: string;
    updated?: string;


constructor(
    id?: any,
    commentString?: string,
    created?: string,
    updated?: string
    
)
{
    this.id = id,
    this.commentString = commentString,
    this.created = created,
    this.updated = updated
}

}
